var DEFAULT = "http://www.google.com/logos/" + logoUrl.url[Math.floor(Math.random()*1000)].doodleurl;

handleLogo = function(doodleUrl) {
  var controller = new Controller(doodleUrl);
  controller.customizeLogo();
};

function Controller(doodleUrl) {
  this.random_doodle = doodleUrl;
}

Controller.prototype.customizeLogo = function() {
  var doc = document;
  var logo = doc.getElementById('logo');
  if (!logo) {
    return;
  }

  var resize = false;
  var holidayToday = false;
  if (window.location.href.search(/q=/) != -1) {
    if (!doc.getElementById('xjsi')) {
      return;
    }
    resize = true;
    var styleArray = doc.getElementsByTagName('style');
    var styleArrayLength = styleArray.length;
    for (var i = 0; i < styleArrayLength; ++i) {
      if (styleArray[i].innerHTML.search(/logos/) != -1) {
        holidayToday = true;
      }
    }
  } else if (logo && (logo.src && logo.src.search(/logos/) != -1) ||
             (logo.style && logo.style.background.search(/logos/) != -1)) {
    holidayToday = true;
  }

  if (!holidayToday) {
    this.swap(logo, resize);
  }
};

Controller.prototype.swap = function(logoElement, small) {
  if (!logoElement) {
    return;
  }
  var doc = document;
  var customLogo = doc.createElement('a');
  customLogo.title = 'Random Doodle';
  var image = doc.createElement('img');
  image.src = this.random_doodle;
  image.border = 0;
  if (small) {
    image.height = 50;
  }
  customLogo.appendChild(image);
  logoElement.parentNode.replaceChild(customLogo, logoElement);
};

handleLogo(DEFAULT);